// 多实例测试.首先输入一个整数ncase,表示测试实例的个数.每个测试实例的输入如下:
// 第一行是整数n (n<100).然后是n行的账单信息,每一行由事物的名字name和对应的花费c组成,长度不超过200.中间会有一个或多个空格,而每一行的开头和结尾没有空格. 0.0 < c < 1000.0.
// 输出
// 每个测试实例对应一行输出,输出总的花费,小数点后保留一位数字.
#include <stdio.h>
int main()
{
    int n,count;
    scanf("%d", n);
scanf("%d",count);
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < count; j++)
        {
            char arr[100];
            fgets(arr,100,stdin);
        }
        
    }
    
    return 0;
}