
#include <stdio.h>

int main() {
    char c; // 声明一个字符变量c

    scanf("%c", &c); // 从标准输入读取一个字符，并将其赋值给变量c

    if (c >= 'a' && c <= 'z') { // 如果c是小写字母
        c = c - ('a' - 'A'); // 将c转换为对应的大写字母
    }

    printf("%c\n", c); // 打印变量c的值并换行

    return 0; // 返回0，表示程序正常结束
}


// 要将一个小写字母转换为大写字母，可以通过减去一个固定的值来实现。
//在ASCII码表中，小写字母的值比对应的大写字母的值大固定的差值。
//例如，小写字母'a'的ASCII码值是97，大写字母'A'的ASCII码值是65，它们的差值是32。

// 因此，要将一个小写字母转换为大写字母，可以将其ASCII码值减去32。
//例如，将小写字母'c'转换为大写字母'C'，可以将其ASCII码值97减去32，得到65，即对应的大写字母'C'的ASCII码值。

