// 大家都知道，手机号是一个11位长的数字串，同时，作为学生，还可以申请加入校园网，如果加入成功，你将另外拥有一个短号。假设所有的短号都是是 6+手机号的后5位，比如号码为13512345678的手机，对应的短号就是645678。
// 现在，如果给你一个11位长的手机号码，你能找出对应的短号吗？
// 输入
// 输入数据的第一行是一个N(N <= 200)，表示有N组输入数据，接下来的N行每一行为一个11位的手机号码。
// 输出
// 输出应包括N行，每行包括一个对应的短号，输出应与输入的顺序一致。
#include <stdio.h>

int main() {
    int n;
    char phone[12]; // 手机号长度为11位，再加一个字符用于存储字符串结束符'\0'

    // 读取手机号码的数量
    scanf("%d", &n);

    // 循环读取每个手机号码并处理
    for (int i = 0; i < n; i++) {
        // 读取手机号码
        scanf("%s", phone);

        // 输出对应的短号，手机号码的后5位
        printf("6%s\n", phone + 6); // phone数组中下标为6的位置开始就是手机号码的后5位
    }

    return 0;
}


// 在这个程序中，我们首先定义了一个整数变量`n`用来存储手机号码的数量，然后定义了一个字符数组`phone`，其长度为12，用来存储11位手机号码加上一个字符串结束符`\0`。

// 使用`scanf`读取`n`的值后，我们进入一个`for`循环，循环`n`次，每次读取一个手机号码。手机号码以字符串的形式读取到`phone`数组中。

// 根据题目的要求，短号是固定的数字`6`加上手机号的后5位。因此，我们可以直接使用`printf`函数输出字符`6`，然后通过指定`phone`数组的偏移量`+ 6`来输出手机号码的后5位。

// 请注意，这个程序假设输入的手机号码是合法的，且格式正确。在实际应用中，应该增加输入验证来确保手机号码的有效性和正确性。