// 输入某人的18位身份证号，输出其出生日期。
// 输入
// 多实例测试。首先输入一个整数n，表示测试实例的个数，然后是n行，每行是一个18位身份证号。
// 输出
// 对于输入的每个身份证号，输出一行，即其对应的出生日期，输出格式为：yyyy-mm-dd。

#include <stdio.h>

int main() {
    int n;
    char id[19]; // 身份证号长度为18位，再加一个字符用于存储字符串结束符'\0'

    // 读取实例个数
    scanf("%d", &n);

    // 循环读取每个身份证号并处理
    for(int i = 0; i < n; i++) {
        // 读取身份证号
        scanf("%s", id);

        // 输出出生年月日，身份证号的第7-14位是出生年月日
        printf("%.4s-%.2s-%.2s\n", id + 6, id + 10, id + 12);
    }

    return 0;
}


// 在这个程序中，我们首先定义了一个整数变量`n`用来存储测试实例的个数，然后定义了一个字符数组`id`，其长度为19，用来存储18位身份证号加上一个字符串结束符`\0`。

// 使用`scanf`读取`n`的值后，我们进入一个`for`循环，循环`n`次，每次读取一个身份证号。身份证号以字符串的形式读取到`id`数组中。

// 在中国的18位身份证号码中，第7到14位代表的是出生年月日，格式为`yyyyMMdd`。因此，我们可以直接使用`printf`函数中的`%.4s`、`%.2s`和`%.2s`格式占位符来提取和格式化这部分信息。`%.4s`表示打印字符串的前4个字符，`%.2s`表示打印字符串的前2个字符。通过指定`id`数组的偏移量，我们可以打印出正确的年、月、日。

// 请注意，这个程序假设输入的身份证号是合法的，且格式正确。在实际应用中，应该增加输入验证来确保身份证号的有效性和正确性。